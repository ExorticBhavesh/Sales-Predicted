// Calculate statistics
export const calculateStats = (data: AdvertisingRecord[]) => {
  const totalSales = data.reduce((sum, d) => sum + d.sales, 0);
  const avgSales = totalSales / data.length;
  const totalAdSpend = data.reduce((sum, d) => sum + d.tv + d.radio + d.newspaper, 0);
  const avgTv = data.reduce((sum, d) => sum + d.tv, 0) / data.length;
  const avgRadio = data.reduce((sum, d) => sum + d.radio, 0) / data.length;
  const avgNewspaper = data.reduce((sum, d) => sum + d.newspaper, 0) / data.length;

  return {
    totalRecords: data.length,
    totalSales: totalSales.toFixed(1),
    avgSales: avgSales.toFixed(2),
    totalAdSpend: totalAdSpend.toFixed(0),
    avgTv: avgTv.toFixed(1),
    avgRadio: avgRadio.toFixed(1),
    avgNewspaper: avgNewspaper.toFixed(1),
    maxSales: Math.max(...data.map(d => d.sales)).toFixed(1),
    minSales: Math.min(...data.map(d => d.sales)).toFixed(1),
  };
};

